public class Score {
    private int score;
    private int words;

    /**
     * @return the score
     */
    public int getScore() {
        return score;
    }

    /**
     * @param score the score to set
     */
    public void setScore(int score) {
        this.score = score;
    }

    /**
     * @return the words
     */
    public int getWords() {
        return words;
    }

    /**
     * @param words the words to set
     */
    public void setWords(int words) {
        this.words = words;
    }
    
}
